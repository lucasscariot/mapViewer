#include "fdf.h"

void	ft_choose_color(int *b, int *v, int *r, int z, int z2)
{
	if (z <= -20 || z2 <= -20)
	{
		*v = 0;
		*b = 180;
		*r = 4;
	}
	else if (z <= -5 || z2 <= -5)
	{
		*v = 40;
		*b = 220;
		*r = 6;
	}
	else if (z <= 0 || z2 <= 0)
	{
		*v = 140;
		*b = 220;
		*r = 2;
	}
	else if (z <= 5 || z2 <= 5) //Green
	{
		*v = 180;
		*b = 13;
		*r = 100;
	}
	else if (z <= 20 || z2 <= 20) //Green
	{
		*v = 155;
		*b = 13;
		*r = 110;
	}
	else if (z <= 40 || z2 <= 40) //Green
	{
		*v = 140;
		*b = 13;
		*r = 120;
	}
	else if (z <= 50 || z2 <= 50) //Maroon
	{
		*v = 135;
		*b = 30;
		*r = 135;
	}
	else if (z <= 73 || z2 <= 73)
	{
		*v = 163;
		*b = 163;
		*r = 163;
	}
	else
	{
		*v = 255;
		*b = 255;
		*r = 255;
	}
}
